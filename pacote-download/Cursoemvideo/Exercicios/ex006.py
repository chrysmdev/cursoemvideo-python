# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada

num = float(input('Digite um número: '))
print(f'Analisando o número {num}.\n O seu dobro é {num*2}.\n O seu triplo é {num*3}.\n E a sua raiz quadrada é {num**(1/2):.3f}.') 

